package ImageEditor;

public class pixel {
    int red;
    int green;
    int blue;

    public pixel(int r, int g, int b){
        red = r;
        green = g;
        blue = b;
    }

    public int getRed(){
        return red;
    }
    public int getGreen(){
        return green;
    }
    public int getBlue(){
        return blue;
    }
}
